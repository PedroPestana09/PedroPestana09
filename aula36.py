"""
Operadores de atribuição
=  +=  -=  *=  /=  //=  **=  %=
Permitem usar a variável sem voltar a usar o nome da mesma
"""

contador = 3
# contador = contador + 1  # Substituir simplificando pela expressão seguinte:
contador += 1  # Simplificou, não sendo necessario voltar a colocar o nome da variável.
print(contador)

print(10 * "-")

contador_2 = 10
contador_2 *= "p"
