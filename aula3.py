"""
DocString (3 aspas) contêm Strings (textos dentro de aspas)
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> nome no python, string -> tipo de dados
Strings são textos que estão dentro de aspas -> "" ou ''
"""
print(1234)

# Aspas simples
print('Pedro Pestana')

# Aspas duplas
print("Pedro Pestana")

#Escape (caracter de escape é a barra invertida \ que dá instrução para não executar ou interpretar o próximo\seguinte caracter)
print("Pedro \"Pestana\"")

# r (caracter que mostra todos os caracteres. Desenvolvedor desta linguagem, criou para mostrar as expressões regulares)
print(r"Pedro \"Pestana\"")

# O caracter Escape ou o r complicam o código, por isso não são usados. Um truque é trocar as aspas, como o exemplo abaixo
print('Pedro "Pestana"')
print("Pedro 'Pestana'")
