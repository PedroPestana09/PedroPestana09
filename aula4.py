# Tipos int e float (int são numeros inteiros, float são numeros com casas decimais)
# int -> Número inteiro 
# O tipo int representa qualquer número 
# positivo ou negativo. int sem sinal é considerado 
# positivo. 
print(11) # int 
print(-11) # int 
print(0) #int

# float -> Número com ponto flutuante. Usar sempre o ponto . para separar porque a virgula , é utilizada para mais operações/comandos 
# O tipo float representa qualquer número 
# positivo ou negativo com ponto flutuante. 
# float sem sinal é considerado positivo. 
print(1.1, 10.11) 
print(0.0, -1.5) 

# A função type mostra o tipo de classe do objecto no Python
print(type('Pestana')) 
print(type(0)) 
print(type(1.1), type(-1.1), type(0.0)) 
print(type(123), type(1.2), type("Pedro Pestana"))